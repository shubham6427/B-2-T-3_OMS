import java.util.Scanner;

class EvenOdd {
    public static void main(String[] args) {
        int n;
        System.out.print("Enter any Number");
        Scanner r = new Scanner(System.in);

        n = r.nextInt();
        if (n % 2 == 0) {
            System.out.print("Even Number");
        } else {
            System.out.print("Odd Number");
            System.out.print("Odd Number");
        }

    }
}
